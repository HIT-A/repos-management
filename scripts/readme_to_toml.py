def format_toml_string(s: str) -> str:
    """Format a string for TOML output."""
    if not s:
        return '""'

    if "\n" in s:
        # Multiline: escape backslashes in content
        escaped = s.replace("\\", "\\\\")
        return '"""\n' + escaped + '\n"""'

    # Single line: escape special characters
    s = s.replace("\\", "\\\\")
    s = s.replace('"', '\\"')
    return f'"{s}"'


def format_toml_dict(d: dict, indent: int = 0) -> str:
    """Format a dict as inline TOML."""
    if not d:
        return "{}"

    parts = []
    for k, v in d.items():
        if isinstance(v, str):
            parts.append(f"{k} = {format_toml_string(v)}")
        elif isinstance(v, dict):
            parts.append(f"{k} = {format_toml_dict(v)}")
        elif v is None:
            parts.append(f'{k} = ""')
        else:
            parts.append(f"{k} = {v}")

    return "{ " + ", ".join(parts) + " }"


def generate_toml(doc: Document) -> str:
    """Generate TOML from document model."""
    lines = []

    # Top-level fields
    if doc.course_name:
        lines.append(f"course_name = {format_toml_string(doc.course_name)}")

    if doc.course_code:
        lines.append(f"course_code = {format_toml_string(doc.course_code)}")

    if doc.repo_type:
        lines.append(f"repo_type = {format_toml_string(doc.repo_type)}")

    # Description always output
    if doc.description:
        lines.append(f"description = {format_toml_string(doc.description)}")

    # Multi-project: output courses
    if doc.repo_type == "multi-project" and doc.courses:
        for course in doc.courses:
            lines.append("")
            lines.append("[[courses]]")
            if course.name:
                lines.append(f"name = {format_toml_string(course.name)}")
            if course.code:
                lines.append(f"code = {format_toml_string(course.code)}")

            # Course-level reviews (flat items with topic)
            for review in course.reviews:
                lines.append("")
                lines.append("[[courses.reviews]]")
                if review.topic:
                    lines.append(f"topic = {format_toml_string(review.topic)}")
                if review.content:
                    lines.append(f"content = {format_toml_string(review.content)}")
                _append_author_lines(lines, review)

            for teacher in course.teachers:
                lines.append("")
                lines.append("[[courses.teachers]]")
                lines.append(f"name = {format_toml_string(teacher.name)}")
                for review in teacher.reviews:
                    lines.append("")
                    lines.append("[[courses.teachers.reviews]]")
                    if review.content:
                        lines.append(f"content = {format_toml_string(review.content)}")
                    _append_author_lines(lines, review)

            for section in course.sections:
                lines.append("")
                lines.append("[[courses.sections]]")
                lines.append(f"title = {format_toml_string(section.title)}")
                for item in section.items:
                    lines.append("")
                    lines.append("[[courses.sections.items]]")
                    if item.content:
                        lines.append(f"content = {format_toml_string(item.content)}")
                    if item.topic:
                        lines.append(f"topic = {format_toml_string(item.topic)}")
                    _append_author_lines(lines, item)

        # Misc items (e.g., MOOC supplementary content)
        if doc.misc:
            misc_items = doc.misc if isinstance(doc.misc, list) else [doc.misc]
            for mi in misc_items:
                lines.append("")
                lines.append("[[misc]]")
                if isinstance(mi, dict):
                    if mi.get("content"):
                        lines.append(f"content = {format_toml_string(mi['content'])}")
                    author = mi.get("author")
                    if author:
                        if isinstance(author, Author):
                            lines.append(
                                f"author = {format_toml_dict(author.to_toml())}"
                            )
                        elif isinstance(author, dict):
                            lines.append(f"author = {format_toml_dict(author)}")

        return "\n".join(lines) + "\n"

    # Normal mode: basic info section
    basic_info_items = []
    if doc.credits:
        basic_info_items.append(f"【学分】: {doc.credits}")
    if doc.grading:
        basic_info_items.append(f"【成绩构成】: {doc.grading}")

    if basic_info_items:
        lines.append("")
        lines.append("[[sections]]")
        lines.append('title = "基本信息"')
        lines.append("")
        lines.append("[[sections.items]]")
        lines.append(f"content = {format_toml_string(chr(10).join(basic_info_items))}")

    # Lecturers (new schema)
    if not doc.lecturers.is_empty():
        lines.append("")
        lines.append("[lecturers]")

        for it in doc.lecturers.intro:
            lines.append("")
            lines.append("[[lecturers.intro]]")
            if it.content:
                lines.append(f"content = {format_toml_string(it.content)}")
            _append_author_lines(lines, it)

        for lec in doc.lecturers.items:
            lines.append("")
            lines.append("[[lecturers.items]]")
            lines.append(f"name = {format_toml_string(lec.name)}")

            for review in lec.reviews:
                lines.append("")
                lines.append("[[lecturers.items.reviews]]")
                if review.content:
                    lines.append(f"content = {format_toml_string(review.content)}")
                _append_author_lines(lines, review)

        for it in doc.lecturers.summary:
            lines.append("")
            lines.append("[[lecturers.summary]]")
            if it.content:
                lines.append(f"content = {format_toml_string(it.content)}")
            _append_author_lines(lines, it)

    # Sections
    for section in doc.sections:
        lines.append("")
        lines.append("[[sections]]")
        lines.append(f"title = {format_toml_string(section.title)}")

        for item in section.items:
            lines.append("")
            lines.append("[[sections.items]]")
            if item.content:
                lines.append(f"content = {format_toml_string(item.content)}")
            if item.topic:
                lines.append(f"topic = {format_toml_string(item.topic)}")
            _append_author_lines(lines, item)

    return "\n".join(lines) + "\n"


def _append_author_lines(lines: list[str], item: SectionItem) -> None:
    """Append author line(s) to TOML output."""
    if item.author is not None:
        if isinstance(item.author, list):
            author_parts = [format_toml_dict(a.to_toml()) for a in item.author]
            lines.append(f"author = [{', '.join(author_parts)}]")
        else:
            lines.append(f"author = {format_toml_dict(item.author.to_toml())}")
    elif item.has_author:
        lines.append('author = { name = "", link = "", date = "" }')


