def normalize_text(text: str) -> str:
    """Normalize text: strip, dedent, normalize line endings."""
    text = text.replace("\r\n", "\n").replace("\r", "\n")
    lines = text.split("\n")
    if len(lines) > 1:
        min_indent = float("inf")
        for line in lines[1:]:
            if line.strip():
                indent = len(line) - len(line.lstrip())
                min_indent = min(min_indent, indent)
        if min_indent > 0 and min_indent != float("inf"):
            lines = [lines[0]] + [
                line[min_indent:] if line.strip() else line for line in lines[1:]
            ]
    return "\n".join(lines).strip()


def decode_shields_component(s: str) -> str:
    """Decode a shields.io URL path component."""
    s = s.replace("%20", " ")
    s = s.replace("%25", "%")
    s = re.sub(r"--", "-", s)
    return s


_DATE_RE = re.compile(r"\d{4}-\d{2}(?:-\d{2})?")


def parse_author_line(line: str) -> list[Author] | Author | None:
    """Parse author attribution line.

    Returns:
    - None if not an author line
    - Author for single author
    - list[Author] for multiple authors

    Formats:
    - > 文 / [name](link)，YYYY-MM
    - > 文 / [name](link)
    - > 文 / 佚名
    - > 文 / 佚名，YYYY-MM
    - > 文 / Name，YYYY-MM  (Chinese comma)
    - > 文 / Name, YYYY-MM  (English comma)
    - > 文 / [name1](link1)，date1，[name2](link2)，date2  (multi-author)
    """
    line = line.strip()
    if not line.startswith(">"):
        return None

    content = line[1:].strip()

    if not content.startswith("文 /"):
        return None

    content = content[3:].strip()  # Remove "文 /"

    if not content:
        return Author()

    authors: list[Author] = []
    remaining = content

    while remaining:
        remaining = remaining.strip()
        if not remaining:
            break

        # Skip leading separators
        if remaining[0] in (",", "，"):
            remaining = remaining[1:].strip()
            continue

        # Try linked author: [name](link)
        m = re.match(r"\[([^\]]*)\]\(([^)]*)\)", remaining)
        if m:
            name = m.group(1)
            link = m.group(2)
            remaining = remaining[m.end() :].strip()
            date = ""

            # Check for comma + date
            if remaining and remaining[0] in (",", "，"):
                after_comma = remaining[1:].strip()
                dm = _DATE_RE.match(after_comma)
                if dm:
                    date = dm.group(0)
                    remaining = after_comma[dm.end() :].strip()
                else:
                    remaining = after_comma

            authors.append(Author(name=name, link=link, date=date))
            continue

        # Try 佚名
        if remaining.startswith("佚名"):
            remaining = remaining[2:].strip()
            date = ""

            if remaining and remaining[0] in (",", "，"):
                after_comma = remaining[1:].strip()
                dm = _DATE_RE.match(after_comma)
                if dm:
                    date = dm.group(0)
                    remaining = after_comma[dm.end() :].strip()
                else:
                    remaining = after_comma

            authors.append(Author(name="佚名", link="", date=date))
            continue

        # Try plain name (no link)
        name_m = re.match(r"^([^,，\[\]]+)", remaining)
        if name_m:
            name_text = name_m.group(1).strip()
            remaining = remaining[name_m.end() :].strip()
            date = ""

            # Check if this looks like a date
            if _DATE_RE.fullmatch(name_text):
                if authors:
                    authors[-1].date = name_text
                continue

            # Check for comma + date after name
            if remaining and remaining[0] in (",", "，"):
                after_comma = remaining[1:].strip()
                dm = _DATE_RE.match(after_comma)
                if dm:
                    date = dm.group(0)
                    remaining = after_comma[dm.end() :].strip()
                else:
                    remaining = after_comma

            authors.append(Author(name=name_text, link="", date=date))
            continue

        # Fallback
        if remaining:
            authors.append(Author(name=remaining, link="", date=""))
        break

    if not authors:
        return Author()

    if len(authors) == 1:
        return authors[0]

    return authors


def parse_badge_url(url: str) -> tuple[str, str, str]:
    """Parse shields.io badge URL."""
    m = re.search(r"/badge/(.+)$", url)
    if not m:
        return ("", "", "")

    path = m.group(1)
    path = path.replace("--", "\x00ENCODED_DASH\x00")
    parts = path.split("-")
    parts = [p.replace("\x00ENCODED_DASH\x00", "-") for p in parts]

    if len(parts) >= 3:
        return (
            decode_shields_component(parts[0]),
            decode_shields_component(parts[1]),
            decode_shields_component(parts[2]),
        )
    elif len(parts) == 2:
        return (
            decode_shields_component(parts[0]),
            "",
            decode_shields_component(parts[1]),
        )
    elif len(parts) == 1:
        return (decode_shields_component(parts[0]), "", "")

    return ("", "", "")


def parse_toml_comment(line: str) -> dict[str, str]:
    """Parse HTML comment containing TOML metadata.

    Formats:
    - <!-- TOML-SECTION: title="..." type="..." -->
    - <!-- TOML-ITEM: id="..." topic="..." author_type="list" has_author="true" -->
    - <!-- TOML-META: repo_type="normal" -->
    - <!-- TOML-LECTURERS: part="intro|items|summary" -->
    """
    m = re.match(r"<!--\s*TOML-(\w+):\s*(.+?)\s*-->", line.strip())
    if not m:
        return {}

    comment_type = m.group(1).lower()
    content = m.group(2)

    result = {"type": comment_type}

    for pair_m in re.finditer(r'(\w+)="([^"]*)"', content):
        result[pair_m.group(1)] = pair_m.group(2)

    return result


