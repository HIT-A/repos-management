#!/usr/bin/env python3
"""
Convert README.md -> readme.toml (bidirectional converter part 2).

This is the reverse converter that parses Markdown files generated by
convert_toml_to_readme.py and reconstructs the original TOML structure.

Design principles:
1. Use H2 sections (## Title) to locate [[sections]]
2. Use author attribution (> 文 / ...) to locate section item boundaries
3. Use HTML comments (<!-- TOML-ITEM: ... -->) as placeholders for items without authors
4. Preserve round-trip idempotency: TOML -> MD -> TOML should be lossless

Supports two schema types:
- normal: Single course with [[sections]]
- multi-project: Multiple courses with [[courses]]
"""

from __future__ import annotations

import argparse
import re
import sys
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

# Python 3.11+ has tomllib in stdlib, otherwise use tomli
try:
    import tomllib
except ModuleNotFoundError:
    import tomli as tomllib  # type: ignore

# For writing TOML
try:
    import tomli_w  # type: ignore
except ImportError:
    tomli_w = None  # Will fall back to manual formatting


# ============================================================================
# Data Models
# ============================================================================


@dataclass
class Author:
    """Author attribution model."""

    name: str = ""
    link: str = ""
    date: str = ""

    def to_toml(self) -> dict:
        """Convert to TOML dict format."""
        return {"name": self.name, "link": self.link, "date": self.date}

    @classmethod
    def from_toml(cls, data: dict | str) -> "Author":
        """Create from TOML data."""
        if isinstance(data, str):
            return cls(name=data)
        return cls(
            name=data.get("name", ""),
            link=data.get("link", ""),
            date=data.get("date", ""),
        )


@dataclass
class SectionItem:
    """A single item within a section."""

    content: str = ""
    author: Author | list[Author] | None = None  # Single or multiple authors
    item_id: str = ""  # From <!-- TOML-ITEM: id="..." -->
    topic: str = ""  # From <!-- TOML-ITEM: topic="..." -->
    has_author: bool = False  # From <!-- TOML-ITEM: has_author="true" -->
    author_is_list: bool = False  # From <!-- TOML-ITEM: author_type="list" -->

    def to_toml(self) -> dict:
        """Convert to TOML dict format."""
        result = {}
        if self.content:
            result["content"] = self.content
        if self.topic:
            result["topic"] = self.topic
        if self.author is not None:
            if isinstance(self.author, list):
                result["author"] = [a.to_toml() for a in self.author]
            else:
                result["author"] = self.author.to_toml()
        elif self.has_author:
            # Empty author dict preserved via comment
            result["author"] = {"name": "", "link": "", "date": ""}
        return result


@dataclass
class Lecturer:
    """Lecturer with reviews."""

    name: str = ""
    reviews: list[SectionItem] = field(default_factory=list)

    def to_toml(self) -> dict:
        """Convert to TOML dict format."""
        result: dict[str, Any] = {"name": self.name}
        if self.reviews:
            result["reviews"] = [r.to_toml() for r in self.reviews]
        return result


@dataclass
class LecturersBlock:
    """New lecturers block.

    Schema:

    [lecturers]
      [[lecturers.intro]]     (list[SectionItem])
      [[lecturers.items]]     (list[Lecturer])
      [[lecturers.summary]]   (list[SectionItem])

    Legacy schema is represented by putting data into `items`.
    """

    intro: list[SectionItem] = field(default_factory=list)
    items: list[Lecturer] = field(default_factory=list)
    summary: list[SectionItem] = field(default_factory=list)

    def is_empty(self) -> bool:
        return not self.intro and not self.items and not self.summary


@dataclass
class Section:
    """A section with title and items."""

    title: str = ""
    items: list[SectionItem] = field(default_factory=list)

    # Special: 授课教师 section
    lecturers: list[Lecturer] = field(default_factory=list)  # legacy parsed shape
    lecturers_block: LecturersBlock | None = None  # new parsed shape

    def to_toml(self) -> dict:
        """Convert to TOML dict format."""
        if self.lecturers or self.lecturers_block is not None:
            return {}
        result: dict[str, Any] = {"title": self.title}
        if self.items:
            result["items"] = [i.to_toml() for i in self.items]
        return result


@dataclass
class Course:  # For multi-project schema
    """A course within a multi-project repo."""

    name: str = ""
    code: str = ""
    reviews: list[SectionItem] = field(default_factory=list)
    teachers: list[Lecturer] = field(default_factory=list)
    sections: list[Section] = field(default_factory=list)

    def to_toml(self) -> dict:
        """Convert to TOML dict format."""
        result: dict[str, Any] = {}
        if self.name:
            result["name"] = self.name
        if self.code:
            result["code"] = self.code
        if self.reviews:
            result["reviews"] = [r.to_toml() for r in self.reviews]
        if self.teachers:
            result["teachers"] = [t.to_toml() for t in self.teachers]
        if self.sections:
            result["sections"] = [s.to_toml() for s in self.sections]
        return result


@dataclass
class Document:
    """Root document model."""

    course_name: str = ""
    course_code: str = ""
    repo_type: str = "normal"  # "normal" or "multi-project"
    description: str = ""
    sections: list[Section] = field(default_factory=list)

    # Lecturers (new schema)
    lecturers: LecturersBlock = field(default_factory=LecturersBlock)

    courses: list[Course] = field(default_factory=list)  # For multi-project
    misc: dict | list[dict] = field(default_factory=dict)  # e.g. MOOC misc

    # Metadata from badges
    credits: str = ""
    hours: str = ""
    grading: str = ""


