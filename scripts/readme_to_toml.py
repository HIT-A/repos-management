def main() -> int:
    parser = argparse.ArgumentParser(
        description="Convert README.md to readme.toml (bidirectional converter)"
    )
    parser.add_argument(
        "--input",
        "-i",
        required=True,
        help="Input README.md file or directory to scan",
    )
    parser.add_argument(
        "--output",
        "-o",
        help="Output TOML file (default: readme.toml in same directory)",
    )
    parser.add_argument(
        "--overwrite",
        action="store_true",
        help="Overwrite existing TOML file",
    )
    parser.add_argument(
        "--verbose",
        "-v",
        action="store_true",
        help="Show detailed parsing information",
    )

    args = parser.parse_args()

    input_path = Path(args.input)

    if input_path.is_file():
        toml_paths = [input_path]
    else:
        toml_paths = sorted(input_path.rglob("README.md"))

    if not toml_paths:
        print("No README.md files found", file=sys.stderr)
        return 1

    for md_path in toml_paths:
        if args.output and len(toml_paths) == 1:
            out_path = Path(args.output)
        else:
            out_path = md_path.parent / "readme.toml"

        if out_path.exists() and not args.overwrite:
            print(f"Skipping {out_path} (use --overwrite to replace)")
            continue

        try:
            content = md_path.read_text(encoding="utf-8")
            parser_obj = MarkdownParser(content)
            doc = parser_obj.parse()
            toml_content = generate_toml(doc)

            out_path.write_text(toml_content, encoding="utf-8", newline="\n")
            print(f"Generated: {out_path}")

            if args.verbose:
                print(f"  Course: {doc.course_code} - {doc.course_name}")
                print(f"  Sections: {len(doc.sections)}")
                print(f"  Lecturers: {len(doc.lecturers.items)}")

        except Exception as e:
            print(f"Error processing {md_path}: {e}", file=sys.stderr)
            return 1

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
