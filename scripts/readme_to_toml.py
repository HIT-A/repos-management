class MarkdownParser:
    """Parse Markdown back to TOML structure."""

    def __init__(self, content: str):
        self.content = content
        self.lines = content.split("\n")
        self.pos = 0
        self.doc = Document()

    def parse(self) -> Document:
        """Parse the entire document."""
        # Check for TOML-META comment (repo_type)
        for line in self.lines:
            comment = parse_toml_comment(line)
            if comment.get("type") == "meta":
                if "repo_type" in comment:
                    self.doc.repo_type = comment["repo_type"]
                break

        # Extract H1 title
        self._parse_title()

        # Skip TOML-META comment if present
        while self.pos < len(self.lines):
            line = self.lines[self.pos].strip()
            if line.startswith("<!-- TOML-META:"):
                self.pos += 1
                continue
            break

        # Parse badges
        self._parse_badges()

        # Parse sections
        if self.doc.repo_type == "multi-project":
            # Multi-project handles its own description parsing
            # (description may contain ## headers, so _parse_description would break)
            self._parse_multi_project()
        else:
            self._parse_description()
            self._parse_sections()

        return self.doc

    def _parse_title(self):
        """Parse H1 title: # CODE - Name"""
        for i, line in enumerate(self.lines):
            if line.startswith("# "):
                title = line[2:].strip()
                parts = title.split(" - ", 1)
                if len(parts) == 2:
                    self.doc.course_code = parts[0].strip()
                    self.doc.course_name = parts[1].strip()
                else:
                    self.doc.course_name = title
                self.pos = i + 1
                return

    def _parse_badges(self):
        """Parse shields.io badges.

        Badges marked with ``<!-- TOML-BADGES: source="grades_summary" -->``
        come from an external grades_summary.toml and should NOT be written
        back to the course TOML (they are regenerated on every forward pass).
        Only badges from ``source="basic_info"`` (or unmarked legacy badges)
        are persisted to the ``基本信息`` section in TOML.
        """
        badges = []
        badge_source = "basic_info"  # default for legacy / unmarked READMEs

        for i in range(self.pos, len(self.lines)):
            line = self.lines[i]

            # Detect badge source marker
            badge_comment = parse_toml_comment(line)
            if badge_comment.get("type") == "badges":
                badge_source = badge_comment.get("source", "basic_info")
                self.pos = i + 1
                continue

            m = re.match(
                r"!\[([^\]]*)\]\((https://img\.shields\.io/badge/[^)]+)\)", line
            )
            if m:
                alt = m.group(1)
                url = m.group(2)
                label, message, color = parse_badge_url(url)
                badges.append((alt, label, message, color))
                self.pos = i + 1
                continue

            if line.strip() and not line.startswith("<!--"):
                break

            self.pos = i + 1

        # Skip storing badge data if they come from external grades_summary
        if badge_source == "grades_summary":
            return

        for alt, label, message, color in badges:
            if label == "学分":
                self.doc.credits = message
            elif label in ("学时构成", "学时分布"):
                self.doc.hours = message
            elif label == "成绩构成":
                pass
            elif message:
                if self.doc.grading:
                    self.doc.grading += f" | {label} {message}"
                else:
                    self.doc.grading = f"{label} {message}"

    def _parse_description(self):
        """Parse description paragraphs before first H2."""
        desc_lines = []

        for i in range(self.pos, len(self.lines)):
            line = self.lines[i]

            if line.startswith("## "):
                self.pos = i
                break

            if not desc_lines and not line.strip():
                continue

            desc_lines.append(line)
        else:
            self.pos = len(self.lines)

        self.doc.description = normalize_text("\n".join(desc_lines))

    def _parse_sections(self):
        """Parse all H2 sections."""
        while self.pos < len(self.lines):
            line = self.lines[self.pos]

            if line.startswith("## "):
                section = self._parse_section()
                if section:
                    if section.title == "授课教师":
                        if section.lecturers_block is not None:
                            self.doc.lecturers = section.lecturers_block
                        else:
                            # Legacy: lecturers parsed as plain list
                            self.doc.lecturers.items = section.lecturers
                    else:
                        self.doc.sections.append(section)
            else:
                self.pos += 1

    def _parse_section(self) -> Section | None:
        """Parse a single H2 section."""
        title_line = self.lines[self.pos]
        title = title_line[3:].strip()
        self.pos += 1

        section = Section(title=title)

        # Check for TOML-SECTION comment
        if self.pos < len(self.lines):
            comment_data = parse_toml_comment(self.lines[self.pos])
            if comment_data.get("type") == "section":
                if "title" in comment_data:
                    section.title = comment_data["title"]
                self.pos += 1

        # Special handling for 授课教师
        if title == "授课教师":
            section.lecturers_block = self._parse_lecturers_block()
            if section.lecturers_block is None:
                section.lecturers = []
            return section

        # Parse items until next H2 or EOF
        current_item = SectionItem()
        content_lines: list[str] = []

        while self.pos < len(self.lines):
            line = self.lines[self.pos]

            if line.startswith("## "):
                break

            # Check for TOML-ITEM comment
            comment_data = parse_toml_comment(line)
            if comment_data.get("type") == "item":
                # Save previous item
                if (
                    content_lines
                    or current_item.topic
                    or current_item.item_id
                    or current_item.has_author
                ):
                    current_item.content = normalize_text("\n".join(content_lines))
                    if (
                        current_item.content
                        or current_item.topic
                        or current_item.item_id
                        or current_item.has_author
                    ):
                        section.items.append(current_item)
                    content_lines = []

                current_item = SectionItem()
                if "id" in comment_data:
                    current_item.item_id = comment_data["id"]
                if "topic" in comment_data:
                    current_item.topic = comment_data["topic"]
                if comment_data.get("has_author") == "true":
                    current_item.has_author = True
                if comment_data.get("author_type") == "list":
                    current_item.author_is_list = True
                self.pos += 1
                continue

            # Check for author attribution
            parsed_author = parse_author_line(line)
            if parsed_author is not None:
                current_item.content = normalize_text("\n".join(content_lines))
                if isinstance(parsed_author, list):
                    current_item.author = parsed_author
                elif current_item.author_is_list:
                    current_item.author = [parsed_author]
                else:
                    current_item.author = parsed_author
                if (
                    current_item.content
                    or current_item.author
                    or current_item.topic
                    or current_item.has_author
                ):
                    section.items.append(current_item)
                current_item = SectionItem()
                content_lines = []
                self.pos += 1
                continue

            content_lines.append(line)
            self.pos += 1

        # Save last item
        if (
            content_lines
            or current_item.topic
            or current_item.item_id
            or current_item.has_author
        ):
            current_item.content = normalize_text("\n".join(content_lines))
            if (
                current_item.content
                or current_item.topic
                or current_item.item_id
                or current_item.has_author
            ):
                section.items.append(current_item)

        return section

    def _parse_lecturers_block(self) -> LecturersBlock:
        """Parse '授课教师' into the new lecturers block.

        If the file is legacy (no TOML-LECTURERS markers), we parse legacy lecturer list
        and store it into `items`.
        """

        # Detect whether this README contains new lecturers markers in this section
        scan = self.pos
        has_marker = False
        while scan < len(self.lines):
            ln = self.lines[scan]
            if ln.startswith("## "):
                break
            c = parse_toml_comment(ln)
            if c.get("type") == "lecturers":
                has_marker = True
                break
            scan += 1

        if not has_marker:
            block = LecturersBlock()
            block.items = self._parse_lecturers_items_until_boundary()
            return block

        block = LecturersBlock()

        while self.pos < len(self.lines):
            line = self.lines[self.pos]
            if line.startswith("## "):
                break

            c = parse_toml_comment(line)
            if c.get("type") == "lecturers":
                part = (c.get("part") or "").strip().lower()
                self.pos += 1

                if part == "intro":
                    block.intro.extend(
                        self._parse_lecturers_free_items_until_boundary()
                    )
                    continue
                if part == "items":
                    block.items.extend(self._parse_lecturers_items_until_boundary())
                    continue
                if part == "summary":
                    block.summary.extend(
                        self._parse_lecturers_free_items_until_boundary()
                    )
                    continue
                continue

            # Tolerate stray blank lines / human edits
            self.pos += 1

        return block

    def _parse_lecturers_free_items_until_boundary(self) -> list[SectionItem]:
        """Parse intro/summary items until next lecturers marker / lecturer list / next section."""

        items: list[SectionItem] = []
        current_item = SectionItem()
        content_lines: list[str] = []

        def _flush_item() -> None:
            nonlocal current_item, content_lines
            if not (
                content_lines
                or current_item.item_id
                or current_item.topic
                or current_item.has_author
                or current_item.author
            ):
                current_item = SectionItem()
                content_lines = []
                return
            current_item.content = normalize_text("\n".join(content_lines))
            if (
                current_item.content
                or current_item.author
                or current_item.topic
                or current_item.has_author
            ):
                items.append(current_item)
            current_item = SectionItem()
            content_lines = []

        while self.pos < len(self.lines):
            line = self.lines[self.pos]
            if line.startswith("## "):
                break
            if line.strip().startswith("<!-- TOML-LECTURERS:"):
                break
            if re.match(r"^- [^\s]", line):
                break

            comment_data = parse_toml_comment(line)
            if comment_data.get("type") == "item":
                _flush_item()
                current_item = SectionItem()
                if "id" in comment_data:
                    current_item.item_id = comment_data["id"]
                if "topic" in comment_data:
                    current_item.topic = comment_data["topic"]
                if comment_data.get("has_author") == "true":
                    current_item.has_author = True
                if comment_data.get("author_type") == "list":
                    current_item.author_is_list = True
                self.pos += 1
                continue

            parsed_author = parse_author_line(line)
            if parsed_author is not None:
                current_item.content = normalize_text("\n".join(content_lines))
                if isinstance(parsed_author, list):
                    current_item.author = parsed_author
                elif current_item.author_is_list:
                    current_item.author = [parsed_author]
                else:
                    current_item.author = parsed_author
                if (
                    current_item.content
                    or current_item.author
                    or current_item.topic
                    or current_item.has_author
                ):
                    items.append(current_item)
                current_item = SectionItem()
                content_lines = []
                self.pos += 1
                continue

            # Skip leading blank lines
            if not line.strip() and not content_lines and not current_item.item_id:
                self.pos += 1
                continue

            content_lines.append(line)
            self.pos += 1

        _flush_item()
        return items

    def _parse_lecturers_items_until_boundary(self) -> list[Lecturer]:
        """Parse lecturer bullet list until next lecturers marker / next section."""

        lecturers: list[Lecturer] = []
        current_lecturer: Lecturer | None = None
        current_review: SectionItem | None = None
        content_lines: list[str] = []

        def _flush_review() -> None:
            nonlocal content_lines, current_review
            if current_lecturer is None:
                return
            if current_review is not None:
                current_review.content = normalize_text("\n".join(content_lines))
                if (
                    current_review.content
                    or current_review.author
                    or current_review.has_author
                ):
                    current_lecturer.reviews.append(current_review)
                current_review = None
            elif content_lines:
                review = SectionItem()
                review.content = normalize_text("\n".join(content_lines))
                if review.content:
                    current_lecturer.reviews.append(review)
            content_lines = []

        while self.pos < len(self.lines):
            line = self.lines[self.pos]

            if line.startswith("## "):
                break
            if line.strip().startswith("<!-- TOML-LECTURERS:"):
                break

            # Check for TOML-ITEM comment
            comment_data = parse_toml_comment(line)
            if comment_data.get("type") == "item":
                _flush_review()
                current_review = SectionItem()
                if "id" in comment_data:
                    current_review.item_id = comment_data["id"]
                if "topic" in comment_data:
                    current_review.topic = comment_data["topic"]
                if comment_data.get("has_author") == "true":
                    current_review.has_author = True
                if comment_data.get("author_type") == "list":
                    current_review.author_is_list = True
                self.pos += 1
                continue

            # Lecturer name (top-level list item)
            if re.match(r"^- [^\s]", line):
                _flush_review()
                if current_lecturer:
                    lecturers.append(current_lecturer)

                current_lecturer = Lecturer()
                current_lecturer.name = line[2:].strip()
                content_lines = []
                current_review = None
                self.pos += 1
                continue

            # Review content (nested list item with 2-space indent)
            if line.startswith("  - "):
                content_lines.append(line[4:])
                self.pos += 1
                continue

            # Indented TOML-ITEM comment (within lecturer)
            if line.strip().startswith("<!-- TOML-ITEM:"):
                comment_data = parse_toml_comment(line.strip())
                if comment_data.get("type") == "item":
                    _flush_review()
                    current_review = SectionItem()
                    if "id" in comment_data:
                        current_review.item_id = comment_data["id"]
                    if "topic" in comment_data:
                        current_review.topic = comment_data["topic"]
                    if comment_data.get("has_author") == "true":
                        current_review.has_author = True
                    if comment_data.get("author_type") == "list":
                        current_review.author_is_list = True
                    self.pos += 1
                    continue

            # Further nested content / author line (4+ spaces)
            if line.startswith("    "):
                parsed_author = parse_author_line(line)
                if parsed_author is not None and current_lecturer is not None:
                    if current_review is not None:
                        current_review.content = normalize_text(
                            "\n".join(content_lines)
                        )
                        if isinstance(parsed_author, list):
                            current_review.author = parsed_author
                        elif current_review.author_is_list:
                            current_review.author = [parsed_author]
                        else:
                            current_review.author = parsed_author
                        if (
                            current_review.content
                            or current_review.author
                            or current_review.has_author
                        ):
                            current_lecturer.reviews.append(current_review)
                        current_review = None
                    else:
                        review = SectionItem()
                        review.content = normalize_text("\n".join(content_lines))
                        if isinstance(parsed_author, list):
                            review.author = parsed_author
                        else:
                            review.author = parsed_author
                        if review.content or review.author:
                            current_lecturer.reviews.append(review)
                    content_lines = []
                    self.pos += 1
                    continue
                content_lines.append(line[2:])
                self.pos += 1
                continue

            # Author attribution at normal indent
            parsed_author = parse_author_line(line)
            if parsed_author is not None and current_lecturer is not None:
                if current_review is not None:
                    current_review.content = normalize_text("\n".join(content_lines))
                    if isinstance(parsed_author, list):
                        current_review.author = parsed_author
                    elif current_review.author_is_list:
                        current_review.author = [parsed_author]
                    else:
                        current_review.author = parsed_author
                    if (
                        current_review.content
                        or current_review.author
                        or current_review.has_author
                    ):
                        current_lecturer.reviews.append(current_review)
                    current_review = None
                else:
                    review = SectionItem()
                    review.content = normalize_text("\n".join(content_lines))
                    if isinstance(parsed_author, list):
                        review.author = parsed_author
                    else:
                        review.author = parsed_author
                    if review.content or review.author:
                        current_lecturer.reviews.append(review)
                content_lines = []
                self.pos += 1
                continue

            if not line.strip():
                self.pos += 1
                continue

            self.pos += 1

        if current_lecturer:
            _flush_review()
            lecturers.append(current_lecturer)

        return lecturers

    # Legacy parser kept for compatibility with old workflow / tests
    def _parse_lecturers(self) -> list[Lecturer]:
        """Parse lecturers section with nested reviews."""
        return self._parse_lecturers_items_until_boundary()

    def _parse_multi_project(self):
        """Parse multi-project structure."""
        current_course: Course | None = None
        description_lines: list[str] = []

        # Pre-scan: check if TOML-COURSES-START marker exists anywhere
        has_courses_marker = any(
            line.strip() == "<!-- TOML-COURSES-START -->"
            for line in self.lines[self.pos :]
        )
        courses_started = False

        while self.pos < len(self.lines):
            line = self.lines[self.pos]

            # Detect courses start marker — everything before it is description
            if line.strip() == "<!-- TOML-COURSES-START -->":
                if description_lines:
                    self.doc.description = normalize_text("\n".join(description_lines))
                    description_lines = []
                courses_started = True
                self.pos += 1
                continue

            # Before courses start, collect description lines
            if not courses_started:
                # If we hit a ## heading and there is NO TOML-COURSES-START marker
                # anywhere in the file, treat first ## as implicit courses start
                # (fallback for human-edited READMEs that never had the marker).
                if line.startswith("## ") and not has_courses_marker:
                    if description_lines:
                        self.doc.description = normalize_text(
                            "\n".join(description_lines)
                        )
                        description_lines = []
                    courses_started = True
                    # Do NOT advance self.pos — let the ## handler below pick it up
                    continue

                # Skip blank lines at the very start
                if not description_lines and not line.strip():
                    self.pos += 1
                    continue
                description_lines.append(line)
                self.pos += 1
                continue

            # Detect misc section marker
            if line.strip() == "<!-- TOML-MISC -->":
                # Save current course and parse misc
                if current_course is not None:
                    self.doc.courses.append(current_course)
                    current_course = None
                self.pos += 1
                self._parse_misc()
                continue

            if line.startswith("## "):
                if current_course is not None:
                    self.doc.courses.append(current_course)

                header = line[3:].strip()
                current_course = Course()
                self.pos += 1

                # Check for TOML-COURSE comment with exact code/name
                if self.pos < len(self.lines):
                    m = re.match(
                        r'<!--\s*TOML-COURSE:\s*code="([^"]*)"\s*name="([^"]*)"\s*-->',
                        self.lines[self.pos].strip(),
                    )
                    if m:
                        current_course.code = m.group(1)
                        current_course.name = m.group(2)
                        self.pos += 1
                        continue

                # Fallback: split header on ' - '
                parts = header.split(" - ", 1)
                if len(parts) == 2:
                    current_course.code = parts[0].strip()
                    current_course.name = parts[1].strip()
                else:
                    current_course.name = header
                continue

            if line.startswith("### ") and current_course is not None:
                sub_title = line[4:].strip()
                self.pos += 1

                # Check if next line is a TOML-COURSE-REVIEWS marker
                is_reviews = False
                if (
                    self.pos < len(self.lines)
                    and self.lines[self.pos].strip() == "<!-- TOML-COURSE-REVIEWS -->"
                ):
                    is_reviews = True
                    self.pos += 1  # skip the marker

                # Determine section title by removing course prefix
                prefix = ""
                if current_course.code and current_course.name:
                    prefix = f"{current_course.code} - {current_course.name} - "
                elif current_course.code:
                    prefix = f"{current_course.code} - "
                elif current_course.name:
                    prefix = f"{current_course.name} - "

                section_title = sub_title
                if prefix and sub_title.startswith(prefix):
                    section_title = sub_title[len(prefix) :]
                elif " - " in sub_title:
                    parts = sub_title.rsplit(" - ", 1)
                    section_title = parts[-1]

                if section_title == "授课教师":
                    current_course.teachers = self._parse_multi_project_teachers()
                elif is_reviews:
                    # Parse as flat review items with topic = section_title
                    review_items = self._parse_multi_project_review_items(section_title)
                    current_course.reviews.extend(review_items)
                else:
                    section = self._parse_multi_project_section(section_title)
                    if section:
                        current_course.sections.append(section)
                continue

            m = re.match(
                r"!\[([^\]]*)\]\((https://img\.shields\.io/badge/[^)]+)\)", line
            )
            if m and current_course is not None:
                self.pos += 1
                continue

            self.pos += 1

        if current_course is not None:
            self.doc.courses.append(current_course)

    def _parse_multi_project_teachers(self) -> list[Lecturer]:
        """Parse teacher list within a multi-project course subsection."""
        teachers: list[Lecturer] = []
        current_teacher: Lecturer | None = None
        content_lines: list[str] = []

        while self.pos < len(self.lines):
            line = self.lines[self.pos]

            if line.startswith("## ") or line.startswith("### "):
                break

            if re.match(r"^- [^\s]", line):
                if current_teacher:
                    if content_lines:
                        review = SectionItem()
                        review.content = normalize_text("\n".join(content_lines))
                        if review.content:
                            current_teacher.reviews.append(review)
                        content_lines = []
                    teachers.append(current_teacher)

                current_teacher = Lecturer()
                current_teacher.name = line[2:].strip()
                content_lines = []
                self.pos += 1
                continue

            if line.startswith("  - "):
                content_lines.append(line[4:])
                self.pos += 1
                continue

            if line.startswith("    "):
                parsed_author = parse_author_line(line)
                if parsed_author is not None and current_teacher is not None:
                    review = SectionItem()
                    review.content = normalize_text("\n".join(content_lines))
                    if isinstance(parsed_author, list):
                        review.author = parsed_author
                    else:
                        review.author = parsed_author
                    if review.content or review.author:
                        current_teacher.reviews.append(review)
                    content_lines = []
                    self.pos += 1
                    continue
                content_lines.append(line[2:])
                self.pos += 1
                continue

            if not line.strip():
                self.pos += 1
                continue

            self.pos += 1

        if current_teacher:
            if content_lines:
                review = SectionItem()
                review.content = normalize_text("\n".join(content_lines))
                if review.content:
                    current_teacher.reviews.append(review)
            teachers.append(current_teacher)

        return teachers

    def _parse_multi_project_section(self, title: str) -> Section | None:
        """Parse a section within a multi-project course."""
        section = Section(title=title)
        current_item = SectionItem()
        content_lines: list[str] = []

        while self.pos < len(self.lines):
            line = self.lines[self.pos]

            if line.startswith("## ") or line.startswith("### "):
                break
            if line.strip() == "<!-- TOML-MISC -->":
                break

            parsed_author = parse_author_line(line)
            if parsed_author is not None:
                current_item.content = normalize_text("\n".join(content_lines))
                if isinstance(parsed_author, list):
                    current_item.author = parsed_author
                else:
                    current_item.author = parsed_author
                if current_item.content or current_item.author:
                    section.items.append(current_item)
                current_item = SectionItem()
                content_lines = []
                self.pos += 1
                continue

            content_lines.append(line)
            self.pos += 1

        if content_lines:
            current_item.content = normalize_text("\n".join(content_lines))
            if current_item.content:
                section.items.append(current_item)

        return section if section.items else None

    def _parse_multi_project_review_items(self, topic: str) -> list[SectionItem]:
        """Parse review items within a multi-project course review section.

        Similar to _parse_multi_project_section but returns flat list of
        SectionItem with topic set.
        """
        items: list[SectionItem] = []
        current_item = SectionItem(topic=topic)
        content_lines: list[str] = []

        while self.pos < len(self.lines):
            line = self.lines[self.pos]

            if line.startswith("## ") or line.startswith("### "):
                break
            if line.strip() == "<!-- TOML-MISC -->":
                break

            parsed_author = parse_author_line(line)
            if parsed_author is not None:
                current_item.content = normalize_text("\n".join(content_lines))
                if isinstance(parsed_author, list):
                    current_item.author = parsed_author
                else:
                    current_item.author = parsed_author
                if current_item.content or current_item.author:
                    items.append(current_item)
                current_item = SectionItem(topic=topic)
                content_lines = []
                self.pos += 1
                continue

            content_lines.append(line)
            self.pos += 1

        if content_lines:
            current_item.content = normalize_text("\n".join(content_lines))
            if current_item.content:
                items.append(current_item)

        return items

    def _parse_misc(self):
        """Parse misc section (e.g. MOOC) into doc.misc as list of content/author items.

        Note: misc content may contain embedded ### headers (e.g. ### 选课指南),
        so we only break on ## headers, not ### ones.
        """
        misc_items: list[dict] = []
        content_lines: list[str] = []

        while self.pos < len(self.lines):
            line = self.lines[self.pos]

            # Only break on ## (H2) headers — ### inside misc is content
            if line.startswith("## ") and not line.startswith("### "):
                break

            parsed_author = parse_author_line(line)
            if parsed_author is not None:
                content = normalize_text("\n".join(content_lines))
                item: dict = {}
                if content:
                    item["content"] = content
                if isinstance(parsed_author, list):
                    item["author"] = (
                        parsed_author[0] if len(parsed_author) == 1 else parsed_author
                    )
                else:
                    item["author"] = parsed_author
                if item:
                    misc_items.append(item)
                content_lines = []
                self.pos += 1
                continue

            content_lines.append(line)
            self.pos += 1

        if content_lines:
            content = normalize_text("\n".join(content_lines))
            if content:
                misc_items.append({"content": content})

        if misc_items:
            self.doc.misc = misc_items


